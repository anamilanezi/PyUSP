piano_keys = ["a", "b", "c", "d", "e", "f", "g"]

# Get all itens from position 2
print(piano_keys[2:])

# Get all itens til position 5 (stops at 4)
print(piano_keys[:5])

# Get itens from 2 to 5 (not included), skipping
print(piano_keys[2:5:2])

# All list skipping 2
print(piano_keys[::2])

# Reverte the list
print(piano_keys[::-1])